# Pastikan y adalah 1D integer
y = np.array(y).flatten().astype(int)

# Hitung grayscale per gambar (mean RGB → brightness)
gray_images = X.mean(axis=3)          # shape: (n_samples, height, width)
brightness = gray_images.reshape(len(X), -1).mean(axis=1)  # rata-rata brightness per gambar

# Atau jika ingin semua pixel (lebih detail, tapi lebih berat):
# brightness_all = gray_images.flatten()  # → semua pixel dari semua gambar

# Pisahkan brightness berdasarkan kelas
brightness_per_class = {}
for i, name in enumerate(class_names):
    mask = (y == i)
    brightness_per_class[name] = brightness[mask]   # rata-rata brightness per gambar per kelas

# --- Plot Overlay Histogram ---
plt.figure(figsize=(10, 6))

for name, vals in brightness_per_class.items():
    plt.hist(vals, bins=50, alpha=0.6, label=name, density=True)  # density=True agar normalisasi sama

plt.xlabel("Brightness (Grayscale rata-rata per gambar)")
plt.ylabel("Density (probabilitas relatif)")
plt.title("Distribusi Brightness Citra per Kelas")
plt.legend()
plt.grid(True, alpha=0.3)
plt.xlim(0, 255)  # asumsi range 0-255
plt.show()

from sklearn.model_selection import train_test_split

X_train, X_test, y_train, y_test = train_test_split(
    X, y,
    test_size=0.2,
    stratify=y
)

X_test.shape

y_test.shape

