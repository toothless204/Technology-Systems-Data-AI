mean_rgb = [] #plot rata-rata rgb per kelas

for i in range(len(class_names)):
    imgs = X[y == i]
    mean_rgb.append(imgs.mean(axis=(0,1,2)))

mean_rgb = np.array(mean_rgb)

plt.figure()
plt.plot(class_names, mean_rgb[:,0], marker='o', label='Red')
