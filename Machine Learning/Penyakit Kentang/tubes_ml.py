r = X[:, :, :, 0].flatten()
g = X[:, :, :, 1].flatten() #plot distribusi intensitas rgb
b = X[:, :, :, 2].flatten()

plt.figure()
plt.hist(r, bins=50, alpha=0.5, label="Red")
plt.hist(g, bins=50, alpha=0.5, label="Green")
